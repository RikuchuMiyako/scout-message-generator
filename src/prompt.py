"""システムプロンプトと、入力からのプロンプト組み立て。

プロンプトキャッシュ設計（prefix match）:
  - SYSTEM_PROMPT … 全社共通・不変。最も安定したプレフィックス。
  - 企業プロファイル … 企業ごとに固定。同一企業の複数スカウトで再利用。
  - ユーザーメッセージ … 求職者ごとに毎回変わる揮発的入力（キャッシュしない）。
レンダリング順は tools -> system -> messages。安定したものを前に、
揮発するものを後ろに置くことでキャッシュヒット率を最大化する。
"""

from __future__ import annotations

from .models import CompanyProfile, ScoutInputs

# ---------------------------------------------------------------------------
# 全社共通システムプロンプト（不変 / キャッシュ対象の先頭ブロック）
# ---------------------------------------------------------------------------
SYSTEM_PROMPT = """\
あなたは、ダイレクトリクルーティングにおけるスカウトメール作成のプロフェッショナルです。
求職者一人ひとりに対し、その人だけに宛てた「刺さる」スカウト文を、企業の風土と
採用担当者のパーソナリティを反映させた自然な日本語で執筆します。

# 最重要原則
- テンプレート的・量産型の文章は絶対に書かないこと。求職者の経歴・志向と、求人内容の
  具体的な接点を見つけ出し、「なぜあなたに送ったのか」が伝わる文章にする。
- 与えられた「企業プロファイル」の企業風土・担当者の性格・文体指針を必ず反映する。
  担当者本人が、その人の語り口で書いたように仕上げること。
- 誇張や事実にない情報の創作は禁止。求人票・求職者情報・企業プロファイルに書かれて
  いない事実（具体的な数値、制度、固有名詞など）を捏造しないこと。

# 構成の指針
スカウト文は概ね以下の流れを意識する（見出しや番号は付けず、自然な本文として書く）:
1. 書き出し: なぜこの人にスカウトを送ったのか（経歴・スキル・志向への具体的な言及）
2. 求人・企業の魅力: 求職者の関心に接続した訴求。企業風土や働く環境を担当者の言葉で
3. 次のアクション: カジュアル面談・返信など、心理的ハードルの低い一歩を提示
4. 結び: 担当者からの一言。温度感は文体指針に従う

# 新卒 / 中途 の書き分け
- 新卒: ポテンシャル・成長環境・人柄や価値観の共感を重視。専門用語を避け、平易に。
- 中途: これまでの実績・専門性への敬意と、その経験が活きる具体的なポジション・裁量を提示。

# 検索軸（年齢帯・経験職種・居住地）の扱い
- スカウト送付の文脈理解に用いる補助情報。年齢や居住地そのものに不用意に触れない
  （「○歳の方」「○○在住の方」のような直接言及はしない）。
- 経験職種は、求職者情報と整合する範囲で訴求の手がかりとして活用する。

# 文字数
- 指定された目標文字数に対し ±10% 程度を目安に収める。本文（署名・宛名を除く）で数える。
- 文字数のために中身を薄めない。情報が不足する場合は簡潔さを優先する。

# 出力形式
- スカウト文の本文のみを出力する。前置き・解説・補足・文字数の報告などは一切含めない。
- 件名が有用な場合は、本文の前に「件名: ...」の1行のみ許容する。
"""


def _bullet_list(items: list[str]) -> str:
    return "\n".join(f"  - {item}" for item in items if str(item).strip()) or "  （特になし）"


def build_company_context(profile: CompanyProfile) -> str:
    """企業プロファイルを system 用のコンテキスト文字列に整形する。

    企業ごとに固定の内容。SYSTEM_PROMPT の直後に置き、キャッシュの2つ目の
    ブレークポイント対象とする。
    """
    sources = "\n".join(
        f"  - {k}: {v}" for k, v in profile.sources.items() if str(v).strip()
    ) or "  （特になし）"

    return f"""\
# 企業プロファイル
このスカウトを送る企業と採用担当者の情報です。文体・トーン・訴求はこの内容に従ってください。

## 企業
- 企業名: {profile.company_name or "（未設定）"}
- 業界・事業: {profile.industry or "（未設定）"}

## 企業風土
{profile.culture or "（未設定）"}

## 採用担当者
- 担当者名: {profile.recruiter_name or "（未設定）"}
- 性格・語り口: {profile.recruiter_personality or "（未設定）"}

## 文体・トーンの指針
{profile.tone or "丁寧かつ誠実で、押し付けがましくない温度感。"}

## 訴求ポイント（自然に織り込む。羅列はしない）
{_bullet_list(profile.selling_points)}

## 避けたい表現・NG事項
{_bullet_list(profile.ng_expressions)}

## 補足
{profile.notes or "（特になし）"}

## 参照元
{sources}
"""


def build_user_message(inputs: ScoutInputs) -> str:
    """求職者ごとに変わる入力をユーザーメッセージ本文に整形する（揮発的）。"""
    return f"""\
以下の条件で、スカウトメールの本文を作成してください。

# 生成条件
- 区分: {inputs.recruit_type}
- 目標文字数: 約 {inputs.target_chars} 文字（±10%目安）

# 検索軸（送付文脈の補助情報・直接言及しない）
- 年齢帯: {inputs.age_range or "（指定なし）"}
- 経験職種: {inputs.experience_jobs or "（指定なし）"}
- 居住地: {inputs.residence or "（指定なし）"}

# 求人票
{inputs.job_posting.strip()}

# 求職者情報
{inputs.candidate_info.strip()}

上記を踏まえ、この求職者だけに宛てた、企業風土と担当者の人柄が伝わるスカウト文を
本文のみで出力してください。
"""
